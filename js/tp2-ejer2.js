
let nota = parseInt(prompt("ingresa un numero"));
if(nota <= 0 || nota <= 2 ){
     alert("muy deficiente");
 }else if(nota <= 3 || nota <= 4){
        alert("Insuficiente");
}else if (nota <= 5 || nota <= 6 ){
        alert("Suficiente")
}else if (nota <=7){
        alert("Bien");
} else if(nota <=8  || nota <=9){
        alert("notable");
}else if(nota <= 10){
        alert("sobresaliente");
}else{
    document.write("ingresa un numero valido")
    alert("numero erroneo")
}

